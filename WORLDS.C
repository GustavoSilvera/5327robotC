	delay(200);
	mainLift.goal = 3300;
	driveFor2(-6);
	rotFor(dir * -90);
	driveFor2(5);
	FourBar.goal = FourBar.min;
	FourBar.PID.isRunning = true;
	mainLift.goal = mainLift.min -300;
	intakeSpeed = INTAKE;//pick up second cone
